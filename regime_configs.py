def get_config_with_fallback(coin, regime):
    """Returns (cfg, reason). Top-1 of ensemble per coin/regime."""
    if coin not in REGIME_CONFIGS:
        return None, 'coin_not_tuned'
    c = REGIME_CONFIGS[coin]
    if regime in c:
        return c[regime], 'direct'
    fb_map = {'bull-storm':'bull-calm','bear-storm':'bear-calm'}
    if regime in fb_map and fb_map[regime] in c:
        return c[fb_map[regime]], f'fallback_{fb_map[regime]}'
    if 'chop' in c: return c['chop'], 'chop_fallback'
    return None, 'no_match'


def get_ensemble(coin, regime):
    """Full top-K ensemble for multi-signal voting."""
    if coin not in REGIME_CONFIGS: return []
    c = REGIME_CONFIGS[coin]
    key = f'_{regime}_ensemble'
    if key in c: return c[key]
    fb_map = {'bull-storm':'bull-calm','bear-storm':'bear-calm'}
    if regime in fb_map:
        fb_key = f'_{fb_map[regime]}_ensemble'
        if fb_key in c: return c[fb_key]
    if '_chop_ensemble' in c: return c['_chop_ensemble']
    return []


def coverage_stats():
    regimes = ('chop','bull-calm','bull-storm','bear-calm','bear-storm')
    out = {}
    for r in regimes:
        n = sum(1 for cfg in REGIME_CONFIGS.values() if r in cfg)
        out[r] = f"{n}/{len(REGIME_CONFIGS)} ({n*100//max(len(REGIME_CONFIGS),1)}%)"
    return out
