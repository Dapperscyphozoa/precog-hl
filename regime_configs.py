def get_config_with_fallback(coin, regime):
    """Returns (cfg, reason). First of top-K ensemble."""
    if coin not in REGIME_CONFIGS: return None, 'coin_not_tuned'
    c = REGIME_CONFIGS[coin]
    if regime in c:
        top = c[regime][0]
        reason = 'fallback_from_'+top['_fallback_from'] if '_fallback_from' in top else 'direct'
        return top, reason
    if 'chop' in c: return c['chop'][0], 'chop_last_resort'
    return None, 'no_regime_match'


def get_ensemble(coin, regime):
    """Returns top-K ensemble for this coin+regime. Empty list if none."""
    if coin not in REGIME_CONFIGS: return []
    c = REGIME_CONFIGS[coin]
    if regime in c: return c[regime]
    if 'chop' in c: return c['chop']
    return []


def coverage_stats():
    regimes = ('chop','bull-calm','bull-storm','bear-calm','bear-storm')
    out = {}
    for r in regimes:
        n_direct = sum(1 for cfg in REGIME_CONFIGS.values() if r in cfg and '_fallback_from' not in cfg[r][0])
        n_fb = sum(1 for cfg in REGIME_CONFIGS.values() if r in cfg and '_fallback_from' in cfg[r][0])
        out[r] = f"direct={n_direct} fallback={n_fb} total={n_direct+n_fb}/{len(REGIME_CONFIGS)}"
    return out
